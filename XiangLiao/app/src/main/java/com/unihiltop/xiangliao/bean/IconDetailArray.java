package com.unihiltop.xiangliao.bean;

public class IconDetailArray {
	public int[] iconArray;
	public String[] nameArray;
	public String[] noteArray;
	public int count;
	public int[] backgroundArray;
	public int[] getIconArray() {
		return iconArray;
	}
	public void setIconArray(int[] iconArray) {
		this.iconArray = iconArray;
	}
	public String[] getNameArray() {
		return nameArray;
	}
	public void setNameArray(String[] nameArray) {
		this.nameArray = nameArray;
	}
	public String[] getNoteArray() {
		return noteArray;
	}
	public void setNoteArray(String[] noteArray) {
		this.noteArray = noteArray;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int[] getBackgroundArray() {
		return backgroundArray;
	}
	public void setBackgroundArray(int[] backgroundArray) {
		this.backgroundArray = backgroundArray;
	}
	
	
}
